    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (!s.getSerial().isOpen())
            JOptionPane.showMessageDialog(frame, "Porta inesistente");
        else {
            JOptionPane.showMessageDialog(frame, "Timer impostato");
            String str="";
            if(jRadioButton1.isSelected())
                str = "s,"+ jSlider1.getValue() + ";";
            else if(jRadioButton2.isSelected())
                str = "m,"+ jSlider1.getValue() + ";";
            else if(jRadioButton3.isSelected())
                str = "h,"+ jSlider1.getValue() + ";";
            s.getSerial().writeSerial(str);
            this.hide();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

