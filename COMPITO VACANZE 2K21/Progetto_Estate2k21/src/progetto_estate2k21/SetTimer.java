    public class AscoltatoreSlider implements javax.swing.event.ChangeListener{
    
        SetTimer adaptee;

        public AscoltatoreSlider(SetTimer adaptee){
            this.adaptee = adaptee;
        }
        
        @Override
        public void stateChanged(ChangeEvent e) {
            jLabel2.setText("Tempo: " + adaptee.jSlider1.getValue() + "h");
            if(jRadioButton1.isSelected())
                 jLabel2.setText("Tempo: " + adaptee.jSlider1.getValue() + "s");
            else if(jRadioButton2.isSelected())
                 jLabel2.setText("Tempo: " + adaptee.jSlider1.getValue() + "m");
            else if(jRadioButton3.isSelected())
                 jLabel2.setText("Tempo: " + adaptee.jSlider1.getValue() + "h");
        }
    }
